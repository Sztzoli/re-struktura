package week11.d02.junior;

public class Ride {

    private int day;
    private int stop;
    private int distance;

    public Ride(int day, int stop, int distance) {
        this.day = day;
        this.stop = stop;
        this.distance = distance;
    }

    public int getDay() {
        return day;
    }

    public int getStop() {
        return stop;
    }

    public int getDistance() {
        return distance;
    }


}
